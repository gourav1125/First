						<h5>Polycystic ovary syndrome (PCOS)</h5>
						<p>A greater percentage of women suffer from diagnosed and undiagnosed Polycystic Ovary Syndrome (PCOS) which occurs due to an imbalance in female and male sex hormones. This results in excess amounts of androgens causing ovarian cysts. Weight gain is a common side effect of this condition and so is hair loss. Due to the presence of male hormones, you may lose hair in your head and start growing hair on the face and other parts of the body. Taking hormonal therapy for PCOS can help you to overcome this condition and stop excessive hair loss. </p>
						<h5>Trichotillomania</h5>
						<p>Trichotillomania is an impulse control disorder which forces you to compulsively pull out your hair. It is four times common in women compared to men. The onset of trichotillomania starts before the age of 17 due to various psychological reasons. If you continue to pull your hair out, it will tear and strip your hair resulting in more pronounced hair loss. Antidepressants are useful in dealing with this psychological condition. However, you need intense behavior therapy to completely control this condition. </p>
						<h5>Overstyling</h5>
						<p>Women use more styling products such as shampoos, conditioners, hair dryers, curlers and much more compared to men. Vigorous styling can cause undue stress on your hair resulting in hair loss. Continued usage of extreme styling such cornrows, hair weaves, tight braids, high heat, harsh chemicals, and chemical relaxers can break your hair at the root, forcing it not to grow back. You should limit the usage of heat driven products and chemical treatments if you want to protect your hair. </p>
						<h5>Alopecia and lupus</h5>
						<p>The autoimmune disorders leading to hair loss such as alopecia and lupus are more pronounced in women than in men. These are caused by an overactive immune system that confuses the body. You may start losing hair in patches and in extreme cases, you may lose all the hair in your body including eyebrows and eyelashes. Steroids and drugs can be useful in combating this condition. In the case of lupus, however, hair loss can result in scarring, preventing hair from growing back. In that case trying new hair styles and using wigs are the only options left. </p>
						<h5>Birth control pills</h5>
						<p>Birth control pills are hormonal pills that greatly influence hormonal levels in your body. Continuous usage of  these pills can result in hair loss. You can consult with your gynecologist and choose another prescription medicine if the one you currency use results in hair loss.</p>
					
